export const mongoWorlds = [
  mongoWorld01,
  mongoWorld02,
  mongoWorld03,
  mongoWorld04,
  mongoWorld05,
  mongoWorld06,
];

export function getMongoLevel(
  levelId: string,
): { world: (typeof mongoWorlds)[0]; level: import('./types').MongoLevel } | undefined {
  for (const w of mongoWorlds) {
    const level = w.levels.find((l) => l.id === levelId);
    if (level) return { world: w, level };
  }
  return undefined;
}

export function getNextMongoLevelId(levelId: string): string | null {
  const found = getMongoLevel(levelId);
  if (!found) return null;
  const { world } = found;
  const idx = world.levels.findIndex((l) => l.id === levelId);
  if (idx >= 0 && idx < world.levels.length - 1) {
    return world.levels[idx + 1]!.id;
  }
  const wi = mongoWorlds.findIndex((w) => w.id === world.id);
  if (wi >= 0 && wi < mongoWorlds.length - 1) {
    return mongoWorlds[wi + 1]!.levels[0]?.id ?? null;
  }
  return null;
}
